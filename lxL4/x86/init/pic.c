#include <asm.h>

void init_pic(void)
{
	io_outb(0x21, 0xff);
	io_outb(0xa1, 0xff);

	io_outb(0x20, 0x11);
	io_outb(0x21, 0x20); // IRQ 0-7 -> INT 20-27
	io_outb(0x21, 0x04);
	io_outb(0x21, 0x01);

	io_outb(0xa0, 0x11);
	io_outb(0xa1, 0x28); // IRQ 8-15 -> INT 28-2f
	io_outb(0xa1, 0x02);
	io_outb(0xa1, 0x01);

	io_outb(0x21, 0xfb);
	io_outb(0xa1, 0xff);
}
